// Function to get translation based on current language
function getTranslation(key) {
    const currentLang = localStorage.getItem('language') || 'en';
    return translations[currentLang][key] || key;
}

// Function to get nested translation (for objects like languageLevel)
function getNestedTranslation(parentKey, childKey) {
    const currentLang = localStorage.getItem('language') || 'en';
    return translations[currentLang][parentKey] && translations[currentLang][parentKey][childKey] || childKey;
}
